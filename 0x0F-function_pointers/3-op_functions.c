#include "3-calc.h"

/**
 * op_add - Addition operation
 *
 * @a: First term
 * @b: Second term
 *
 * Return: Result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtraction operation
 *
 * @a: First term
 * @b: Second term
 *
 * Return: Result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication operation
 *
 * @a: First term
 * @b: Second  term
 *
 * Return: Result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division operation
 *
 * @a: First term
 * @b: Second term
 *
 * Return: Result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo operation
 *
 * @a: First term
 * @b: Second term
 *
 * Return: Result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
